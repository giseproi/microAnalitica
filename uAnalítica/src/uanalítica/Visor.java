    private void cargarImagen(){
        try {
            
            this.miBufferedImage=ImageIO.read(miArchivo);
            int ancho =this.miBufferedImage.getWidth();
            int alto=this.miBufferedImage.getHeight();
            System.out.println("("+ancho+","+alto+")");
            ImageIcon icon = new ImageIcon(miBufferedImage);
            jLabel1.setIcon(icon);
            jLabel1.setSize(ancho, alto);
            jPanel1.add(jLabel1);
            
        } catch (IOException ex) {
           System.out.println("Ocurrió una excepción");
        }
    }

