    private void jLabel1MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseMoved
int x, y;
        x=evt.getX();
        y=evt.getY();
        
        
        
        int colorRGB=this.miBufferedImage.getRGB(x, y);
        
        Color miColor = new Color(colorRGB, true);
        
        
        this.valorR.setText(Integer.toString(miColor.getRed()));
        this.valorG.setText(Integer.toString(miColor.getGreen()));
        this.valorB.setText(Integer.toString(miColor.getBlue()));          // TODO add your handling code here:
    }//GEN-LAST:event_jLabel1MouseMoved

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        int opcion=miNavegador.showOpenDialog(this);
        
        if (opcion == JFileChooser.APPROVE_OPTION) {
        this.miArchivo = miNavegador.getSelectedFile();
        this.cargarImagen();
        }
    }//GEN-LAST:event_jButton1MouseClicked

